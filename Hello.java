class EvenCheck {
    public static void main(String[] args)
{
    int a=10;
    int b=15;
    if(a/2==0){
        System.out.println(a + " is Even");
    } else {
        System.out.println(a + " is odd");
    }
    if(b/2==0){
      System.out.println(b + " is Even");
    } else{
        System.out.println(b + " is odd"); 
    }
}
}